
public class TestaWhile {
	
	public static void main (String[] args) {
		
		int contador = 0;
		
		while (contador <= 19) {
			//contador = contador + 1;
			//contador += 2;
			contador++;
			System.out.println(contador);
		}
		
		System.out.println("\n" + "O número final do contador é -> " + contador);
	}

}
